// Напиши цикл, який пропонує в prompt ввести число, більше 100. 
// Якщо відвідувач ввів інше число – попросити ввести ще раз и т.д.


let number;

do {
  number = prompt("Введіть число, більше 100:", 0);
} while (number <= 100 && number !== null);

if (number !== null) {
  console.log("Ви ввели число більше 100: " + number);
} else {
  console.log("Ви натиснули 'Відміна'.");
}